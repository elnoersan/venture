// An enum that defines the different types of rewards for completing quests
public enum RewardType
{
    Currency,   // A reward in the form of in-game currency (e.g., gold, coins)
    Experience, // A reward in the form of experience points (e.g., for leveling up)
    Item        // A reward in the form of an item (e.g., a weapon, potion, or resource)
}